public class NumbersLeft {

	public static void main(String[] args) {

		System.out.println(1);

		for (int i = 1; i < 3; i++) {
			System.out.print(2);
		}

		System.out.println();
		
		for (int j = 1; j < 4; j++) {
			System.out.print(3);

		}

		System.out.println();

		for (int j = 1; j < 5; j++) {
			System.out.print(4);

		}

		System.out.println();

		for (int j = 1; j < 6; j++) {
			System.out.print(5);

		}


		
	}

}

